n = int(input())
print("*" * n + " " * (2 * n - 3) + "*" * n)  # 시작
for i in range(n - 2):  # 본문
    print(" " * (i + 1) + "*" + " " * (n - 2) + "*" + " " * ((n - 2 - i) * 2 - 1) + "*" + " " * (n - 2) + "*")
print(" " * (n - 1) + "*" + " " * (n - 2) + "*" + " " * (n - 2) + "*")
for i in range(n - 3, -1, -1):  # 본문의 역순
    print(" " * (i + 1) + "*" + " " * (n - 2) + "*" + " " * ((n - 2 - i) * 2 - 1) + "*" + " " * (n - 2) + "*")
print("*" * n + " " * (2 * n - 3) + "*" * n)  # 끝